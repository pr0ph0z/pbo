public class PernyataanSWITCH1 {
	public static void main(String[] args){
		int pilihan = 3;
		switch(pilihan){
		      case 1:
			System.out.println("Soto Ayam");
			break;
		      case 2:
			System.out.println("Gule Kambing");
			break;
		      case 3:
			System.out.println("Nasi Goreng");
			break;
		      default:
			System.out.println("Silakan Pilih 1, 2 atau 3");
		}
	}
}